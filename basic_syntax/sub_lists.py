channels = [
    'Facebook',
    'Instagram',
    'Feria',
    'WhatsApp',
    'Linkedin',
    'HI5'
]

print(channels[1:3])  # Rango especificó
print(channels[:3])  # Desde el inicio hasta el índice 3
print(channels[2:])  # Desde el índice 2 hasta el final
print(channels[1:6:2])  # Rango especificó con saltos de a dos
print(channels[::2])  # saltos de a dos
